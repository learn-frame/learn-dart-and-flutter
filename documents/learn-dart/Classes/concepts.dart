/// 类
///
/// Dart 中的类是支持基于 mixin 继承机制的面向对象的语言, 所有对象都是一个类的实例.
///
/// Dart 的类中没有 public, private, protected 等限定符, 标识符以下划线 (_) 开头则
/// 表示该标识符在库内是私有的.
///
/// 所有的类都是继承于 Object 类.
///
/// 基于 mixin 的继承机制, 意味着每个除 Object 类之外的类都只有一个超类, 一个类的
/// 代码可以在其它多个类继承中重复使用.
///
/// Extension 方法是一种在不更改类或创建子类的情况下向类添加功能的方式.
